export function getLessonsByCourse(courseId: string): Lesson[] {
  return lessons
    .filter(lesson => lesson.courseId === courseId)
    .sort((a, b) => a.order - b.order);
}

export function getLessonsByModule(courseId: string, moduleId: string): Lesson[] {
  return lessons
    .filter(lesson => lesson.courseId === courseId && lesson.moduleId === moduleId)
    .sort((a, b) => a.order - b.order);
}

export function getLessonById(lessonId: string): Lesson | undefined {
  return lessons.find(lesson => lesson.id === lessonId);
}

export function getTotalLessons(courseId: string): number {
  return lessons.filter(lesson => lesson.courseId === courseId).length;
}
